static bool oled_ok = false;

static bool oled_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};  // Co=0, D/C=0 (command)
    return i2c_write_timeout_us(OLED_I2C, OLED_ADDR, buf, 2, false, I2C_TIMEOUT_US) == 2;
}

bool oled_init(void) {
    // I2C already initialized by ina219_init() — don't re-init

    // SSD1306 init sequence
    const uint8_t init_cmds[] = {
        0xAE,       // Display OFF
        0xD5, 0x80, // Clock div
        0xA8, 0x3F, // Mux ratio 64
        0xD3, 0x00, // Display offset 0
        0x40,       // Start line 0
        0x8D, 0x14, // Charge pump ON
        0x20, 0x00, // Horizontal addressing mode
        0xA1,       // Segment remap (flip horizontal)
        0xC8,       // COM scan decrement (flip vertical)
        0xDA, 0x12, // COM pins config
        0x81, 0xCF, // Contrast
        0xD9, 0xF1, // Pre-charge
        0xDB, 0x40, // VCOMH deselect
        0xA4,       // Display from RAM
        0xA6,       // Normal display (not inverted)
        0xAF,       // Display ON
    };

    for (size_t i = 0; i < sizeof(init_cmds); i++) {
        if (!oled_cmd(init_cmds[i])) {
            oled_ok = false;
            return false;
        }
    }

    oled_ok = true;
    oled_clear();
    oled_update();
    return true;
}

void oled_clear(void) {
    memset(fb, 0, sizeof(fb));
}

// Set a single pixel in the frame buffer
static void set_pixel(uint8_t x, uint8_t y) {
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT) return;
    fb[x + (y / 8) * OLED_WIDTH] |= (1 << (y & 7));
}

void oled_text(uint8_t x, uint8_t y, const char *str) {
    while (*str) {
        char c = *str++;
        if (c < 32 || c > 122) c = '?';
        const uint8_t *glyph = &font5x7[(c - 32) * 5];
        for (uint8_t col = 0; col < 5; col++) {
            uint8_t bits = glyph[col];
            for (uint8_t row = 0; row < 7; row++) {
                if (bits & (1 << row)) {
                    set_pixel(x + col, y + row);
                }
            }
        }
        x += 6;  // 5px char + 1px gap
    }
}

void oled_hbar(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t fill_pct) {
    if (fill_pct > 100) fill_pct = 100;
    uint8_t fill_w = (uint8_t)((uint16_t)w * fill_pct / 100);

    // Border
    for (uint8_t i = 0; i < w; i++) {
        set_pixel(x + i, y);
        set_pixel(x + i, y + h - 1);
    }
    for (uint8_t j = 0; j < h; j++) {
        set_pixel(x, y + j);
        set_pixel(x + w - 1, y + j);
    }

    // Fill
    for (uint8_t i = 1; i < fill_w && i < w - 1; i++) {
        for (uint8_t j = 1; j < h - 1; j++) {
            set_pixel(x + i, y + j);
        }
    }
}

void oled_update(void) {
    if (!oled_ok) return;

    // Set column and page address to cover full display
    oled_cmd(0x21); oled_cmd(0); oled_cmd(127);  // Column 0-127
    oled_cmd(0x22); oled_cmd(0); oled_cmd(7);     // Page 0-7

    // Send framebuffer in chunks (I2C max ~32 bytes per transaction on some hw)
    for (uint16_t i = 0; i < sizeof(fb); i += 16) {
        uint8_t buf[17];
        buf[0] = 0x40;  // Co=0, D/C=1 (data)
        memcpy(&buf[1], &fb[i], 16);
        i2c_write_timeout_us(OLED_I2C, OLED_ADDR, buf, 17, false, I2C_TIMEOUT_US);
    }
}
