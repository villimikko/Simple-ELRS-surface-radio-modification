// ---------------------------------------------------------------------------
// Text rendering
// ---------------------------------------------------------------------------

void display_draw_char(uint16_t x, uint16_t y, char ch, uint16_t fg, uint16_t bg) {
    uint8_t c = (uint8_t)ch;
    if (c < 32 || c > 127) c = 32;
    const uint8_t *glyph = font_8x16[c - 32];

    // Pixel buffer for one character (8x16 = 128 pixels)
    // Pre-swapped to big-endian for ST7789
    uint16_t char_buf[FONT_WIDTH * FONT_HEIGHT];
    uint16_t be_fg = (fg << 8) | (fg >> 8);
    uint16_t be_bg = (bg << 8) | (bg >> 8);
    uint16_t *p = char_buf;

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            *p++ = (bits & 0x80) ? be_fg : be_bg;
            bits <<= 1;
        }
    }

    display_set_window(x, y, x + FONT_WIDTH - 1, y + FONT_HEIGHT - 1);

    dc_data();
    cs_select();
    spi_write_blocking(spi0, (uint8_t *)char_buf, sizeof(char_buf));
    cs_deselect();
}

void display_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t fg, uint16_t bg) {
    while (*str) {
        if (x + FONT_WIDTH > DISPLAY_WIDTH) break;
        display_draw_char(x, y, *str, fg, bg);
        x += FONT_WIDTH;
        str++;
    }
}

// ---------------------------------------------------------------------------
// Horizontal bar (channel visualization)
// ---------------------------------------------------------------------------

void display_draw_hbar(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                       uint8_t fill_pct, uint16_t fg, uint16_t bg) {
    if (fill_pct > 100) fill_pct = 100;
    uint16_t fill_w = (uint16_t)((uint32_t)w * fill_pct / 100);
    uint16_t empty_w = w - fill_w;

    if (fill_w > 0) display_fill_rect(x, y, fill_w, h, fg);
    if (empty_w > 0) display_fill_rect(x + fill_w, y, empty_w, h, bg);
}
