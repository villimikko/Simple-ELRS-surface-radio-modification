// ST7789 LCD driver for Waveshare RP2350-LCD-1.47-A
// 172x320 pixel display, SPI0 + DMA, landscape rotation (320x172)
// Init sequence from official Waveshare demo code

#include <string.h>
#include "pico/stdlib.h"
#include "hardware/spi.h"
#include "hardware/dma.h"
#include "hardware/gpio.h"
#include "hardware/pwm.h"

#include "display.h"
#include "hardware_config.h"

// Pin aliases from hardware_config.h
#define PIN_SCK     PIN_LCD_CLK    // GP18 — SPI0 SCK
#define PIN_CS      PIN_LCD_CS     // GP17 — SPI0 CSn
#define PIN_DC      PIN_LCD_DC     // GP16 — data/command
#define PIN_MOSI    PIN_LCD_MOSI   // GP19 — SPI0 TX
#define PIN_RST     PIN_LCD_RST    // GP20 — reset
#define PIN_BL      PIN_LCD_BL     // GP21 — backlight

// Window offset for 172-pixel panel
// From Waveshare code: offset 0x22 (34) applied to the short axis
// In VERTICAL mode (landscape for us): offset on Y axis
#define OFFSET_X  0
#define OFFSET_Y  34

// DMA channel
static int dma_chan = -1;

// Line buffer for fill operations
static uint16_t line_buf[DISPLAY_WIDTH];

// ---------------------------------------------------------------------------
// Low-level SPI helpers
// ---------------------------------------------------------------------------

static inline void cs_select(void) {
    gpio_put(PIN_CS, 0);
}

static inline void cs_deselect(void) {
    gpio_put(PIN_CS, 1);
}

static inline void dc_command(void) {
    gpio_put(PIN_DC, 0);
}

static inline void dc_data(void) {
    gpio_put(PIN_DC, 1);
}

static void send_cmd(uint8_t cmd) {
    dc_command();
    cs_select();
    spi_write_blocking(spi0, &cmd, 1);
    cs_deselect();
}

static void send_data8(uint8_t data) {
    dc_data();
    cs_select();
    spi_write_blocking(spi0, &data, 1);
    cs_deselect();
}

static void send_data_buf(const uint8_t *data, uint32_t len) {
    dc_data();
    cs_select();
    spi_write_blocking(spi0, data, len);
    cs_deselect();
}

// ---------------------------------------------------------------------------
// Backlight PWM (matches Waveshare's DEV_SET_PWM)
// ---------------------------------------------------------------------------

static void backlight_set(uint8_t brightness) {
    // PWM on backlight pin, 0-100
    gpio_set_function(PIN_BL, GPIO_FUNC_PWM);
    uint slice = pwm_gpio_to_slice_num(PIN_BL);
    pwm_set_wrap(slice, 100);
    pwm_set_chan_level(slice, pwm_gpio_to_channel(PIN_BL), brightness);
    pwm_set_enabled(slice, true);
}

// ---------------------------------------------------------------------------
// ST7789 initialization (from Waveshare official LCD_1in47.c)
// ---------------------------------------------------------------------------

void display_init(void) {
    // --- GPIO init first (matches Waveshare order) ---
    gpio_init(PIN_RST);
    gpio_set_dir(PIN_RST, GPIO_OUT);

    gpio_init(PIN_DC);
    gpio_set_dir(PIN_DC, GPIO_OUT);
    gpio_put(PIN_DC, 0);   // Waveshare starts DC low

    gpio_init(PIN_CS);
    gpio_set_dir(PIN_CS, GPIO_OUT);
    gpio_put(PIN_CS, 1);

    // --- SPI0 at 30 MHz (Waveshare uses 30000*1000) ---
    spi_init(spi0, 30000 * 1000);

    gpio_set_function(PIN_SCK, GPIO_FUNC_SPI);
    gpio_set_function(PIN_MOSI, GPIO_FUNC_SPI);

    // --- PWM backlight (matches Waveshare: clkdiv=50, wrap=100, chan B) ---
    gpio_set_function(PIN_BL, GPIO_FUNC_PWM);
    uint slice = pwm_gpio_to_slice_num(PIN_BL);
    pwm_set_wrap(slice, 100);
    pwm_set_chan_level(slice, PWM_CHAN_B, 1);
    pwm_set_clkdiv(slice, 50);
    pwm_set_enabled(slice, true);

    // --- Hardware reset (Waveshare sequence) ---
    gpio_put(PIN_RST, 1);
    sleep_ms(100);
    gpio_put(PIN_RST, 0);
    sleep_ms(100);
    gpio_put(PIN_RST, 1);
    sleep_ms(100);

    // --- Set backlight to 90% ---
    pwm_set_chan_level(slice, PWM_CHAN_B, 90);

    // --- Set scan direction: VERTICAL = landscape (MADCTL 0x70) ---
    send_cmd(0x36);       // MADCTL
    send_data8(0x70);     // VERTICAL mode: MX + MV

    // --- Init registers (exact Waveshare sequence) ---
    send_cmd(0x11);       // Sleep out
    sleep_ms(120);

    send_cmd(0x3A);       // COLMOD
    send_data8(0x05);     // 16-bit color (RGB565)

    send_cmd(0xB2);       // Porch control
    send_data8(0x0C);
    send_data8(0x0C);
    send_data8(0x00);
    send_data8(0x33);
    send_data8(0x33);

    send_cmd(0xB7);       // Gate control
    send_data8(0x35);

    send_cmd(0xBB);       // VCOM setting
    send_data8(0x35);

    send_cmd(0xC0);       // LCM control
    send_data8(0x2C);

    send_cmd(0xC2);       // VDV and VRH command enable
    send_data8(0x01);

    send_cmd(0xC3);       // VRH set
    send_data8(0x13);

    send_cmd(0xC4);       // VDV set
    send_data8(0x20);

    send_cmd(0xC6);       // Frame rate control
    send_data8(0x0F);

    send_cmd(0xD0);       // Power control
    send_data8(0xA4);
    send_data8(0xA1);

    send_cmd(0xD6);
    send_data8(0xA1);

    // Positive voltage gamma
    send_cmd(0xE0);
    static const uint8_t pvgam[] = {
        0xF0, 0x00, 0x04, 0x04, 0x04, 0x05, 0x29,
        0x33, 0x3E, 0x38, 0x12, 0x12, 0x28, 0x30
    };
    send_data_buf(pvgam, sizeof(pvgam));

    // Negative voltage gamma
    send_cmd(0xE1);
    static const uint8_t nvgam[] = {
        0xF0, 0x07, 0x0A, 0x0D, 0x0B, 0x07, 0x28,
        0x33, 0x3E, 0x36, 0x14, 0x14, 0x29, 0x32
    };
    send_data_buf(nvgam, sizeof(nvgam));

    send_cmd(0x21);       // Display inversion on
    send_cmd(0x11);       // Sleep out (again, per Waveshare)
    sleep_ms(120);
    send_cmd(0x29);       // Display on

    // --- Claim DMA channel ---
    dma_chan = dma_claim_unused_channel(true);

    // --- Backlight on (90% brightness) ---
    backlight_set(90);

    // Clear to black (UI rendering starts immediately after on Core 1)
    display_fill(COLOR_BLACK);
}

// ---------------------------------------------------------------------------
// Window and pixel transfer
// ---------------------------------------------------------------------------

void display_set_window(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
    // VERTICAL mode (landscape): X maps to columns, Y maps to rows
    // Waveshare applies offset 0x22 (34) on Y in VERTICAL mode
    uint16_t xs = x0;
    uint16_t xe = x1;
    uint16_t ys = y0 + OFFSET_Y;
    uint16_t ye = y1 + OFFSET_Y;

    // Column address set (CASET)
    send_cmd(0x2A);
    uint8_t col_data[] = {xs >> 8, xs & 0xFF, xe >> 8, xe & 0xFF};
    send_data_buf(col_data, 4);

    // Row address set (RASET)
    send_cmd(0x2B);
    uint8_t row_data[] = {ys >> 8, ys & 0xFF, ye >> 8, ye & 0xFF};
    send_data_buf(row_data, 4);

    // Memory write
    send_cmd(0x2C);
}

void display_send_pixels(uint16_t *buf, uint32_t count) {
    if (count == 0) return;

    // ST7789 expects big-endian RGB565, but our buffer is little-endian.
    // Byte-swap each pixel before sending.
    // (Waveshare does this too: Color = ((Color << 8) & 0xff00) | (Color >> 8))
    for (uint32_t i = 0; i < count; i++) {
        buf[i] = (buf[i] << 8) | (buf[i] >> 8);
    }

    dc_data();
    cs_select();

    // Use DMA for 8-bit SPI transfers (pixel data is byte array now)
    dma_channel_config cfg = dma_channel_get_default_config(dma_chan);
    channel_config_set_transfer_data_size(&cfg, DMA_SIZE_8);
    channel_config_set_dreq(&cfg, spi_get_dreq(spi0, true));
    channel_config_set_read_increment(&cfg, true);
    channel_config_set_write_increment(&cfg, false);

    dma_channel_configure(
        dma_chan,
        &cfg,
        &spi_get_hw(spi0)->dr,
        buf,
        count * 2,    // byte count
        true
    );

    dma_channel_wait_for_finish_blocking(dma_chan);

    while (spi_is_busy(spi0)) {
        tight_loop_contents();
    }

    cs_deselect();
}

// ---------------------------------------------------------------------------
// Drawing primitives
// ---------------------------------------------------------------------------

void display_fill_rect(uint16_t x0, uint16_t y0, uint16_t w, uint16_t h, uint16_t color) {
    if (w == 0 || h == 0) return;
    if (x0 + w > DISPLAY_WIDTH)  w = DISPLAY_WIDTH - x0;
    if (y0 + h > DISPLAY_HEIGHT) h = DISPLAY_HEIGHT - y0;

    // Pre-swap color to big-endian for ST7789
    uint16_t be_color = (color << 8) | (color >> 8);
    uint16_t line_w = (w > DISPLAY_WIDTH) ? DISPLAY_WIDTH : w;
    for (uint16_t i = 0; i < line_w; i++) {
        line_buf[i] = be_color;
    }

    display_set_window(x0, y0, x0 + w - 1, y0 + h - 1);

    dc_data();
    cs_select();

    // Send line buffer for each row (already byte-swapped)
    for (uint16_t row = 0; row < h; row++) {
        spi_write_blocking(spi0, (uint8_t *)line_buf, line_w * 2);
    }

    cs_deselect();
}

void display_fill(uint16_t color) {
    display_fill_rect(0, 0, DISPLAY_WIDTH, DISPLAY_HEIGHT, color);
}

